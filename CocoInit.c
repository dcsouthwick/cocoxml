#include  <stdio.h>
#include  <stdlib.h>
#include  <string.h>

static const char * const suffixes[] = {
    "Parser", "Scanner", NULL
};

static int
CocoConvert(const char * srcprefix, const char * tgtprefix,
	    const char * srcfn, const char * tgtfn)
{
    char lnbuf[4096];
    FILE * srcfp, * tgtfp;
    char * start0, * start1, * cur;
    const char * const * suffix;
    int srcprefixlen = strlen(srcprefix);

    if (!(srcfp = fopen(srcfn, "r"))) {
	fprintf(stderr, "Can not open %s for read.\n", srcfn);
	goto errquit0;
    }
    if (!(tgtfp = fopen(tgtfn, "w"))) {
	fprintf(stderr, "Can not open %s for write.\n", tgtfn);
	goto errquit1;
    }

    while (fgets(lnbuf, sizeof(lnbuf), srcfp)) {
	start0 = start1 = lnbuf;
	for (;;) {
	    cur = strstr(start1, srcprefix);
	    if (cur == NULL) break;
	    for (suffix = suffixes; *suffix; ++suffix)
		if (!strncmp(cur + srcprefixlen, *suffix, strlen(*suffix))) {
		    *cur = 0; fputs(start0, tgtfp);
		    fputs(tgtprefix, tgtfp);
		    fputs(*suffix, tgtfp);
		    start0 = start1 = cur + srcprefixlen + strlen(*suffix);
		    break;
		}
	    if (!*suffix) start1 = cur + srcprefixlen;
	}
	fputs(start0, tgtfp);
    }

    fclose(tgtfp);
    fclose(srcfp);
    return 0;
 errquit1:
    fclose(srcfp);
 errquit0:
    return -1;
}

int
main(int argc, char * argv[])
{
    int rc, index;
    if (argc < 5 || argc % 2 == 0) {
	fprintf(stderr, "%s SRC-PREFIX TGT-PREFIX SRC-FILE TGT-FILE ...\n", argv[0]);
	return -1;
    }
    rc = 0;
    for (index = 3; index < argc; index += 2)
	if (CocoConvert(argv[1], argv[2], argv[index], argv[index + 1]) < 0)
	    rc = -1;
    return rc;
}
