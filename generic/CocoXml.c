#include  <stdio.h>
#include  <string.h>

static const char * usage_format =
    "Usage: %s Grammar.atg {{Option}}\n"
    "Options:\n"
    "  -namespace <namespaceName>\n"
    "  -frames    <frameFilesDirectory>\n"
    "  -trace     <traceString>\n"
    "  -o         <outputDirectory>\n"
    "Valid characters in the trace string:\n"
    "  A  trace automaton\n"
    "  F  list first/follow sets\n"
    "  G  print syntax graph\n"
    "  I  trace computation of first sets\n"
    "  J  list ANY and SYNC sets\n"
    "  P  print statistics\n"
    "  S  list symbol table\n"
    "  X  list cross reference table\n"
    "XmlScanner[.lang].frame and XmlParser[.lang].frame files needed in ATG directory\n"
    "or in a directory specified in the -frames option.\n";

int
main(int argc, char * argv[])
{
    int          idx, retVal = 0;
    const char * nsName = NULL;
    const char * frameDir = NULL;
    const char * ddtString = NULL;
    const char * outDir = NULL;
    const char * srcName = NULL;
    const char * traceFileName = NULL;

    printf("CocoXml/R (Oct22, 2008)\n");
    for (idx = 1; idx < argc; ++idx) {
	if (!strcmp(argv[idx], "-namespace") && idx + 1 < argc) nsName = argv[++idx];
	else if (!strcmp(argv[idx], "-frames") && idx + 1 < argc) frameDir = argv[++idx];
	else if (!strcmp(argv[idx], "-trace") && idx + 1 < argc) ddtString = argv[++idx];
	else if (!strcmp(argv[idx], "-o") && idx + 1 < argc) outDir = argv[++idx];
	else srcName = argv[idx];
    }
    if (argc > 1 && srcName != NULL) {
    } else {
	printf(usage_format, argv[0]);
    }
    return retVal;
}
