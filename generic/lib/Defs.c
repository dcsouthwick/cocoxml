void *
_CocoMalloc_(size_t size, const char * fname, int line)
{
    void * ptr;
    if ((ptr = malloc(size))) return ptr;
    fprintf(stderr, "malloc failed in %s#%d!\n", fname, line);
    exit(-1);
}

void *
_CocoRealloc_(void * ptr, size_t size, const char * fname, int line)
{
    if ((ptr = realloc(ptr, size))) return ptr;
    fprintf(stderr, "realloc failed in %s#%d!\n", fname, line);
    exit(-1);
}

void
_CocoFree_(void * ptr, const char * fname, int line)
{
    free(ptr);
}

char *
_CocoStrdup_(const char * str, const char * fname, int line)
{
    char * ptr;
    if ((ptr = strdup(str))) return ptr;
    fprintf(stderr, "strdup failed in %s#%d!\n", fname, line);
    exit(-1);
}

void *
_AllocObject_(void * self, size_t szobj, const char * fname, int line)
{
    if (self) return self;
    return _CocoMalloc_(szobj, fname, line);
}

int
UTF8Get(const char ** str, int EoF)
{
    int ch, c1, c2, c3, c4;
    const char * cur = *str;
    ch = *cur++;
    if (ch >= 128 && ((ch & 0xC0) != 0xC0) && (ch != EoF)) {
	fprintf(stderr, "Inside UTF-8 character!\n");
	exit(-1);
    }
    if (ch < 128 || ch == EoF) return ch;
    if ((ch & 0xF0) == 0xF0) {
	/* 1110xxx 10xxxxxx 10xxxxxx 10xxxxxx */
	c1 = ch & 0x07;
	ch = *cur++; if (ch == 0) goto broken;
	c2 = ch & 0x3F;
	ch = *cur++; if (ch == 0) goto broken;
	c3 = ch & 0x3F;
	ch = *cur++; if (ch == 0) goto broken;
	c4 = ch & 0x3F;
	*str = cur;
	return (((((c1 << 6) | c2) << 6) | c3) << 6) | c4;
    }
    if ((ch & 0xE0) == 0xE0) {
	/* 1110xxxx 10xxxxxx 10xxxxxx */
	c1 = ch & 0x0F;
	ch = *cur++; if (ch == 0) goto broken;
	c2 = ch & 0x3F;
	ch = *cur++; if (ch == 0) goto broken;
	c3 = ch & 0x3F;
	*str = cur;
	return (((c1 << 6) | c2) << 6) | c3;
    }
    /* (ch & 0xC0) == 0xC0 */
    /* 110xxxxx 10xxxxxx */
    c1 = ch & 0x1F;
    ch = *cur++; if (ch == 0) goto broken;
    c2 = ch & 0x3F;
    *str = cur;
    return (c1 << 6) | c2;
 broken:
    fprintf(stderr, "Broken in UTF8 character.\n");
    exit(-1);
}

void
DumpBuffer(DumpBuffer_t * self, char * buf, size_t szbuf)
{
    self->start = buf;
    self->cur = self->start; *self->cur = 0;
    self->last = buf + szbuf;
}

void
DumpBuffer_Print(DumpBuffer_t * self, const char * format, ...)
{
    va_list ap;

    va_start(ap, format);
    self->cur += vsnprintf(self->cur, self->last - self->cur, format, ap);
    va_end(ap);
}

void
DumpBuffer_Clear(DumpBuffer_t * self)
{
    self->cur = self->start; *self->cur = 0;
}

Bool_t
DumpBuffer_Full(DumpBuffer_t * self)
{
    return self->cur == self->last;
}

const char *
UnescapeCh(DumpBuffer_t * buf, const char * s)
{
    int ch; const char * sbk;
    if (*s != '\\') {
	DumpBuffer_Print(buf, "%c", *s); return s + 1;
    }
    switch (*s) {
    case '\\': DumpBuffer_Print(buf, "%c",  '\\'); return s + 2;
    case 'a': DumpBuffer_Print(buf, "%c", '\a'); return s + 2;
    case 'b': DumpBuffer_Print(buf, "%c", '\b'); return s + 2;
    case 'f': DumpBuffer_Print(buf, "%c", '\f'); return s + 2;
    case 'n': DumpBuffer_Print(buf, "%c", '\n'); return s + 2;
    case 'r': DumpBuffer_Print(buf, "%c", '\r'); return s + 2;
    case 't': DumpBuffer_Print(buf, "%c", '\t'); return s + 2;
    case 'v': DumpBuffer_Print(buf, "%c", '\v'); return s + 2;
    case '\'': DumpBuffer_Print(buf, "%c", '\''); return s + 2;
    case '\"': DumpBuffer_Print(buf, "%c", '\"'); return s + 2;
    case '0': s += 2; sbk = s; ch = 0;
	while (s - sbk < 3)
	    if (*s >= '0' && *s <= '7') { ch = (ch << 3) | (*s++ - '0'); }
	    else break;
	break;
    case 'x': s += 2; sbk = s; ch = 0;
	while (s - sbk < 3)
	    if (*s >= '0' && *s <= '9') ch = (ch << 4) | (*s++ - '0');
	    else if (*s >= 'A' && *s <= 'F') ch = (ch << 4) | (*s++ - 'A' + 10);
	    else if (*s >= 'a' && *s <= 'f') ch = (ch << 4) | (*s++ - 'a' + 10);
	    else break;
    default:
	return NULL;
    }
    DumpBuffer_Print(buf, "%c", ch);
    return s;
}

char *
Unescape(const char * s)
{
    char dbufspace[1024]; DumpBuffer_t dbuf;
    DumpBuffer(&dbuf, dbufspace, sizeof(dbufspace));
    if (*s == '"') ++s;
    while (*s) {
	if (s[0] == '"' && s[1] == 0) break;
	if (!(s = UnescapeCh(&dbuf, s))) return NULL; /* Error encountered. */
    }
    return CocoStrdup(dbufspace);
}

void
EscapeCh(DumpBuffer_t * buf, int ch)
{
    switch (ch) {
    case '\\': DumpBuffer_Print(buf, "\\\\"); break;
    case '\a': DumpBuffer_Print(buf, "\\a"); break;
    case '\b': DumpBuffer_Print(buf, "\\b"); break;
    case '\f': DumpBuffer_Print(buf, "\\f"); break;
    case '\n': DumpBuffer_Print(buf, "\\n"); break;
    case '\r': DumpBuffer_Print(buf, "\\r"); break;
    case '\t': DumpBuffer_Print(buf, "\\t"); break;
    case '\v': DumpBuffer_Print(buf, "\\v"); break;
    case '\'': DumpBuffer_Print(buf, "\\'"); break;
    case '\"': DumpBuffer_Print(buf, "\\\""); break;
    default:
	if (ch < ' ' || ch > 127) DumpBuffer_Print(buf, "\\x%03x", ch);
	else DumpBuffer_Print(buf, "%c", ch);
	break;
    }
}

void
Escape(DumpBuffer_t * buf, const char * s)
{
    while (*s && !DumpBuffer_Full(buf)) EscapeCh(buf, *s);
}

