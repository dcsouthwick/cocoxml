#include  <stdio.h>
#include  <stdlib.h>
#include  <string.h>
#include  "SortedList.h"
#include  "Symbol.h"

struct SLEntry_s {
    Symbol_t * key;
    void * value;
};

SortedList_t *
SortedList(SortedList_t * self)
{
    self = AllocObject(self, sizeof(SortedList_t));
    self->Count = 0; self->Capacity = 32;
    self->Data = CocoMalloc(sizeof(SLEntry_t) * self->Capacity);
    return self;
}

void
SortedList_Destruct(SortedList_t * self)
{
    SLEntry_t * cur;
    if (self->Data) {
	for (cur = self->Data; cur - self->Data < self->Count; ++cur) {
	    Symbol_Destruct(cur->key); CocoFree(cur->key);
	    /* cur->value is not freed yet. */
	}
	CocoFree(self->Data);
    }
}

void
SortedList_Set(SortedList_t * self, Symbol_t * key, void * value)
{
    int b, m, e, rc;
    if (self->Count >= self->Capacity) {
	for (self->Capacity = self->Capacity + self->Capacity;
	     self->Count >= self->Capacity;
	     self->Capacity = self->Capacity + self->Capacity);
	self->Data = CocoRealloc(self->Data,
				 self->Capacity * sizeof(SLEntry_t));
    }
    b = 0; e = self->Count;
    while (b < e) {
	m = (b + e) / 2;
	rc = Symbol_CompareTo(self->Data[m].key, key);
	if (rc < 0) b = m + 1;
	else if (rc == 0) { /* Exist. overwrite. */
	    fprintf(stderr, "Conflict symbol: %s(%d), %s(%d)\n",
		    self->Data[m].key->name, self->Data[m].key->n,
		    key->name, key->n);
	    exit(-1);
	} else {
	    e = m;
	}
    }
    if (b < self->Count)
	memmove(self->Data + b + 1, self->Data + b,
		sizeof(SLEntry_t) * (self->Count - b));
    self->Data[b].key = key;
    self->Data[b].value = value;
    ++self->Count;
}

Symbol_t *
SortedList_GetKey(const SortedList_t * self, int index)
{
    return self->Data[index].key;
}

static int
SLCmp(const void * key, const void * entry)
{
    return Symbol_CompareTo((const Symbol_t *)key,
			    ((const SLEntry_t *)entry)->key);
}

void *
SortedList_Get(const SortedList_t * self, const Symbol_t * key)
{
    SLEntry_t * entry;
    if (self->Count == 0) return NULL;
    entry = bsearch(key, self->Data, self->Count, sizeof(SLEntry_t), SLCmp);
    return entry ? entry->value : NULL;
}
