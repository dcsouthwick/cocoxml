#include  "Node.h"

Node_t *
Node(Node_t * self, int typ, Symbol_t * sym, int line)
{
    self->n = 0;
    self->typ = typ;
    self->next = NULL;
    self->down = NULL;
    self->sub = NULL;
    self->up = 0;
    self->sym = sym;
    self->val = 0;
    self->code = 0;
    self->set = NULL;
    self->pos = NULL;
    self->line = line;
    self->state = NULL;
    return self;
}
