#include  <stdlib.h>
#include  "Graph.h"

Graph_t *
Graph(Graph_t * self)
{
    self = AllocObject(self, sizeof(Graph_t));
    self->l = self->r = NULL;
    return self;
}

Graph_t *
GraphLR(Graph_t * self, Node_t * left, Node_t * right)
{
    self = AllocObject(self, sizeof(Graph_t));
    self->l = left; self->r = right;
    return self;
}

Graph_t *
GraphP(Graph_t * self, Node_t * p)
{
    self = AllocObject(self, sizeof(Graph_t));
    self->l = self->r = p;
    return self;
}
