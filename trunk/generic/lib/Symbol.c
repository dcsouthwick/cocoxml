#include  <stdio.h>
#include  <stdlib.h>
#include  <string.h>
#include  "Symbol.h"
#include  "Node.h"

Symbol_t *
Symbol(Symbol_t * self, int typ, const char * name, int line)
{
    self = AllocObject(self, sizeof(Symbol_t));
    self->n = 0;
    self->typ = typ;
    self->name = CocoStrdup(name);
    self->graph = NULL;
    self->tokenKind = 0;
    self->deletable = FALSE;
    self->firstReady = FALSE;
    self->first = NULL;
    self->follow = NULL;
    self->nts = NULL;
    self->line = line;
    self->attrPos = NULL;
    self->semPos = NULL;
    return self;
}

void
Symbol_Destruct(Symbol_t * self)
{
    if (self->name) CocoFree(self->name);
}

int
Symbol_CompareTo(const Symbol_t * self, const Symbol_t * x)
{
    return strcmp(self->name, x->name);
}

static const char * tKind[] = {
    "fixedToken", "classToken", "litToken", "classLitToken"
};

void
Symbol_Dump(const Symbol_t * self, DumpBuffer_t * buf)
{
    char name[13];
    snprintf(name, sizeof(name), "%s             ", self->name);
    DumpBuffer_Print(buf, "%3d %14s %s", self->n, name, nTyp[self->typ]);
    if (self->attrPos == NULL) DumpBuffer_Print(buf, " false ");
    else DumpBuffer_Print(buf, " true  ");
    if (self->typ == node_nt) {
	DumpBuffer_Print(buf, "%5d", Node_Num(self->graph));
	if (self->deletable)  DumpBuffer_Print(buf, " true  ");
	else  DumpBuffer_Print(buf, " false ");
    } else {
	DumpBuffer_Print(buf, "            ");
    }
    DumpBuffer_Print(buf, "%5d %s", self->line, tKind[self->tokenKind]);
}
